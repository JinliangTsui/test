package com.ego.po;

public class AddressInfo {
	
	private Integer adrId;
	private String memUserName;
	private String receiverName;
	private String receiverTel;
	private String receiverAdr;
	private String zip;

	public Integer getAdrId() {
		return adrId;
	}

	public void setAdrId(Integer adrId) {
		this.adrId = adrId;
	}

	public String getMemUserName() {
		return memUserName;
	}

	public void setMemUserName(String memUserName) {
		this.memUserName = memUserName;
	}

	public String getReceiverName() {
		return receiverName;
	}

	public void setReceiverName(String receiverName) {
		this.receiverName = receiverName;
	}

	public String getReceiverTel() {
		return receiverTel;
	}

	public void setReceiverTel(String receiverTel) {
		this.receiverTel = receiverTel;
	}

	public String getReceiverAdr() {
		return receiverAdr;
	}

	public void setReceiverAdr(String receiverAdr) {
		this.receiverAdr = receiverAdr;
	}

	public String getZip() {
		return zip;
	}

	public void setZip(String zip) {
		this.zip = zip;
	}

}
